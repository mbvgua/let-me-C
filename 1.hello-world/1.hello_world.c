#include <stdio.h>

int main() {
    printf("Hello world\n"); // \n denotes the new line and must be defined explicitly
    printf("Answer = %d\n", 42); //%d denotes a digit
    printf("My name is %s\n", "Nakamura"); //%s denotes  a string
    printf("x is %.1f and y is %d\n", 3.5232,120); //%f denotes a floating point number .1 say to 0.0
}